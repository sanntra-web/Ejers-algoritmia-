"""Ejercicio 6: Tres personas aportan dinero para fundar una empresa (no necesariamente en
partes iguales). Calcular qué porcentaje invirtió cada una."""
persona1 = 500000
persona2 = 2000000
persona3 = 250000
total = persona1 + persona2 + persona3 
porcentaje1 = (persona1 / total) * 100
porcentaje2 = (persona2 / total) * 100
porcentaje3 = (persona3 / total) * 100
print("porcentaje1 :", porcentaje1,"porcentaje2 :", porcentaje2,"porcentaje3 :", porcentaje3)
