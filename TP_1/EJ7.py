"""Ejercicio 7: Una persona invierte su capital en un banco y desea saber cuánto dinero ganará
en un mes, teniendo en cuenta que el banco paga 2% mensual. ¿Cuánto ganará
en seis meses si deja su dinero invertido?"""
Inversion = 1000000
interes_mensual = 0.02
ganancia_mensual = Inversion * interes_mensual
ganancia_seis_meses = ganancia_mensual * 6
print("Ganancia mensual:", ganancia_mensual)
print("Ganancia en seis meses:", ganancia_seis_meses)
